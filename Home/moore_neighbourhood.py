def count_neighbours(grid, row, col):
    count = 0
    for i in range(-1, 2):
        for j in range(-1, 2):
            if row+j >= 0 and col+i >= 0:
                try:
                    if grid[row+j][col+i] == 1:
                        count += 1
                except IndexError:
                    pass
    if grid[row][col] == 1:
        count -= 1
    return count

